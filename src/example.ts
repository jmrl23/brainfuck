import brainfuck from './main';

function main() {
  const toEncode = 'Hello, World!';
  const toDecode =
    '+++++++++++++++++++++++++++++++++++++[>++>++>++>++>++>++>++>++>++>++>++>++>++<<<<<<<<<<<<<-]>--.>+++++++++++++++++++++++++++.>++++++++++++++++++++++++++++++++++.>++++++++++++++++++++++++++++++++++.>+++++++++++++++++++++++++++++++++++++.>------------------------------.>------------------------------------------.>+++++++++++++.>+++++++++++++++++++++++++++++++++++++.>++++++++++++++++++++++++++++++++++++++++.>++++++++++++++++++++++++++++++++++.>++++++++++++++++++++++++++.>-----------------------------------------.';

  console.log('encoded:');
  console.log(brainfuck.encode(toEncode));

  console.log('decoded:');
  console.log(brainfuck.decode(toDecode));
}

void main();
